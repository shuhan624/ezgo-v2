      login_as(@admin_has_permissions, scope: :admin)
      update_data_on_home_slide_form
      click_button("更新首頁輪播")
      find('.tabs-group .tabs-btn').click_link('中文')
      expect(page).to have_content('新的標題')
      expect(page).to have_content('成功更新 首頁輪播')
