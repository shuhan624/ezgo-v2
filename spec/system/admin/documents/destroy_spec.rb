  def destroy_document_on_edit_page
    documents = create_list(:document, 5)
    target_document = documents.first
    visit edit_admin_document_path(target_document)
    find('.float-btns .fa-trash-can').click
    page.driver.browser.switch_to.alert.accept
  end

  def destroy_document_on_show_page
    documents = create_list(:document, 5)
    target_document = documents.first
    visit admin_document_path(target_document)
    find('.float-btns .fa-trash-can').click
    page.driver.browser.switch_to.alert.accept
  end
