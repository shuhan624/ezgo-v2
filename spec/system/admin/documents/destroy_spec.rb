  before(:all) do
    @permit_role = create(:role, permissions: {
      document: {
        index: true,
        show: true,
        edit: true,
        destroy: true,
      }
    })

    @no_permit_role = create(:role, permissions: {
      document: {
        index: true,
        show: true,
        edit: true,
      }
    })

    @admin_has_permissions = create(:admin, role: @permit_role)
    @no_permissions_admin = create(:admin, role: @no_permit_role)
  end

  after(:all) do
    Admin.delete_all
    Role.delete_all
  end

