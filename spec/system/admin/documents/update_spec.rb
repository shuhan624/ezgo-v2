    it "按下「返回」按鈕，可以回到列表頁" do
      find('.back-arrow a.btn').click
      expect(page).to have_current_path(admin_documents_path)
      expect(page).to have_text('檔案列表')
    end

    it "按下「詳細」按鈕，可以進到該則檔案的編輯頁" do
      expect(page).to have_selector('a.btn', text: '詳細')
      find('.btn', text: '詳細').click
      expect(page).to have_current_path(admin_document_path(document))
    end

    it "按下「刪除」按鈕，可以刪除該則檔案" do
      expect(page).to have_css('.float-btns .fa-trash-can')
      find('.float-btns .fa-trash-can').click
      accept_alert '確定進行此操作？'
      notice = find('.bootstrap-notify-container')
      expect(notice).to have_content('成功删除 檔案')
      count = Document.count
      expect(count).to eq(0)
    end
